    private void loadMedicalImages() {

        DefaultTableModel model =
            (DefaultTableModel) tblMedicalImages.getModel();
        model.setRowCount(0);

        try {
            String sql =
                "SELECT m.image_id, p.name, m.image_type, " +
                "m.image_description, m.image_path " +
                "FROM medical_images m " +
                "JOIN patients p ON m.patient_id = p.patient_id " +
                "ORDER BY m.image_id DESC";

            PreparedStatement pst =
                MyConnection.getConnection().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("image_id"),
                    rs.getString("name"),
                    rs.getString("image_type"),
                    rs.getString("image_description"),
                    rs.getString("image_path")
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

