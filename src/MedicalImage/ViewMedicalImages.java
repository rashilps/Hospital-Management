    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        // TODO add your handling code here:
        int selectedRow = tblMedicalImages.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a lab report to view");
            return;
        }

        String path =
        tblMedicalImages.getValueAt(selectedRow, 4).toString();

        try {
            Desktop.getDesktop().open(new File(path));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Unable to open image");
        }
    }//GEN-LAST:event_jButton5MouseClicked

