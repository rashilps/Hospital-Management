private void loadPatients() {
        try {
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            String sql = "SELECT name FROM patients";
            PreparedStatement pst = MyConnection.getConnection().prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                model.addElement(rs.getString("name"));
            }
            jComboBox1.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

// Fetch details on Get Details button click
private void getPatientDetails() {
    String selectedName = (String) jComboBox1.getSelectedItem();
    if (selectedName != null) {
        try {
            String sql = "SELECT * FROM patients WHERE name = ?";
            PreparedStatement pst = MyConnection.getConnection().prepareStatement(sql);
            pst.setString(1, selectedName);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                jLabelIdValue.setText(String.valueOf(rs.getInt("patient_id")));
                jTextFieldName.setText(rs.getString("name"));
                jLabelAgeValue.setText(rs.getString("age"));
                jLabelGenderValue.setText(rs.getString("gender"));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
}
private void updatePatient() {
    String idText = jLabelIdValue.getText();
    if (idText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please load patient details first!");
        return;
    }

    String newName = jTextFieldName1.getText().trim();
    
    String newAge = jAgeTextField.getText().trim();
    
    String newGender = (String) jComboBoxGender.getSelectedItem(); // âœ… Gender
    

    if (newName.isEmpty() || newAge.isEmpty() ) {
        JOptionPane.showMessageDialog(this, "All fields are required!");
        return;
    }

    try {
        

        String sql = "UPDATE patients SET name = ?, age = ?, gender = ? WHERE patient_id = ?";
        PreparedStatement pst = MyConnection.getConnection().prepareStatement(sql);
        pst.setString(1, newName);
        pst.setString(2, newAge);
        pst.setString(3, newGender);
        pst.setInt(4, Integer.parseInt(idText));

        if (pst.executeUpdate() > 0) {
            JOptionPane.showMessageDialog(this, "Patient updated successfully!");
            parent.showPanel(new PatientRecords(parent));
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}
